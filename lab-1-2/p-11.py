# Задача 11. 
# Написать программу, которая выводит на экран вопрос и 5 вариантов 
# ответа. Пользователь должен ввести с клавиатуры номер выбранного варианта 
# ответа. Программа должна проверить его ответ и сообщить результат.
print("Какой язык программирования используется в этой программе?")
options = ["1) JavaScript", "2) Java", "3) Python", "4) C++", "5) Ruby"]
for opt in options:
    print(opt)

answer = input("Введите номер ответа: ")
if answer == "3":
    print("Правильно!")
else:
    print("Неправильно. Правильный ответ — 3.")
