# Задача 5. 
# Написать программу, которая запрашивает у пользователя 
# последовательность чисел (не более 10 шт.). Программа должна вывести 
# введенные числа в порядке возрастания.
numbers = input("Введите до 10 чисел через пробел: ").split()
if len(numbers) <= 10 and all(n.replace('-', '', 1).isdigit() for n in numbers):
    numbers = list(map(int, numbers))
    print("Отсортированные числа:", sorted(numbers))
else:
    print("Ошибка: нужно ввести не более 10 чисел.")
