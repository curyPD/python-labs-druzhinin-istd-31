# Задача 8. 
# Написать программу, которая запрашивает у пользователя целое число 
# больше 5. Так же программа должна сгенерировать число от 0 до 5. Оба числа 
# должны быть переданы в функцию, созданную в задаче №6. На экран должен 
# быть выведен результат работы функции. 
# Дополнительно необходимо проверить введенное пользователем число. 
import random

def multiply(a, b):
    return a * b

user_input = input("Введите целое число больше 5: ")
if user_input.isdigit() and int(user_input) > 5:
    user_number = int(user_input)
    rand_number = random.randint(0, 5)
    print("Случайное число:", rand_number)
    print("Результат умножения:", multiply(user_number, rand_number))
else:
    print("Ошибка: число должно быть целым и больше 5.")
